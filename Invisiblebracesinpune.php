                                      <p>Clear aligners is the ‘invisible’ way to straighten teeth. Forget metal brackets or archwires which are needed for braces, instead Invisalign uses a series of clear, removable aligners to gradually straighten your teeth.</p>

                                        <p>How does Invisalign work?</p>

                                        <p>Invisalign aligners look like a sports mouthguard but are thinner and less noticeable. Made of clear, strong, medical grade, non-toxic plastic, the aligners are custom-made to fit over your upper and lower teeth.</p>

                                        <p>These aligners gradually move your teeth into the desired position. Every 2 to 3 weeks you’ll be given a new set of aligners which build on the work of the previous ones. How long the aligners take to move your teeth into position will depend on how much treatment you require, but on average, Invisalign takes between 12 and 14 months.</p>

                                         <h3 class="hr-after"><strong>What are the benefits of Invisalign?    </strong> </h3> 

                                        <ul class="bullet-list list-unstyled">
                                            <li> Discreet treatment: Invisalign aligners are virtually invisible so you can straighten your teeth without anyone knowing. They’re also removable so you can eat and drink whatever you want.</li>

                                            <li>Easy to clean: Traditional metal braces can make it difficult to floss and brush your teeth properly, which can cause decalcification. This can lead to unsightly marks on your teeth and possibly even decay. Because Invisalign aligners are removable, you can brush and floss normally, making it easier to maintain good oral hygiene.</li>

                                            <li>Less risk: In order to move teeth, force needs to be applied. One of the problems that can arise from this is root resorption. This condition refers to the breakdown of the root of a tooth and could lead to the loss of the tooth. Because the forces exerted by Invisalign aligners are more gradual than those applied by traditional braces, early indications are that Invisalign treatment exhibits a much lower rate of root resorption.</li>


                                            <li>Safer on teeth: When traditional metal braces are removed from teeth, this can sometimes cause some of the tooth's enamel to be removed as well. Because Invisalign doesn't involve using adhesive to bond brackets to teeth, enamel scouring is not an issue.</li>
