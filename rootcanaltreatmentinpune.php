                                         <h3 class="hr-after"><strong>Root Canal Treatment in Pune</strong>
                                 </h3>
                                      <p>Root canal treatment in Pune is the removal of the tooth's pulp, a small, thread-like tissue in the centre of the tooth. Once the damaged, diseased or dead pulp is removed, the remaining space is cleaned, shaped and filled. This procedure seals off the root canal. Years ago, teeth with diseased or injured pulps were removed. Today, root canal treatment saves many teeth that would otherwise be lost.</p>

                                        <p>The most common causes of pulp damage or death are:</p>

									    <ul class="bullet-list list-unstyled">
                                         <li>A cracked tooth</li>
                                         <li>A deep cavity</li>
                                         <li>An injury to a tooth, such as a severe knock to the tooth, either recent or in the past.</li> 
                                        </ul>
 
                                         <p>Once the pulp is infected or dead, if left untreated, pus can build up at the root tip in the jawbone, forming an abscess. An abscess can destroy the bone surrounding the tooth and cause pain.</p>

                                         
                                            <p><b>How long will the restored tooth last?</b></p>

                                            <p>Your treated and restored tooth, or teeth, can last a lifetime with proper care. Because tooth decay can still occur in treated teeth, good oral hygiene and regular dental exams are necessary to prevent further problems.</p>
                                            
                                            <p>As there is no longer a pulp keeping the tooth alive, root-treated teeth can become brittle and are more prone to fracture. This is an important consideration when deciding whether to crown or fill a tooth after root canal treatment.</p>
                                            

