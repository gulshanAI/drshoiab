                                      <p><b>About Sensitive Teeth in Pune</b></p>

                                        <p>If a taste of ice cream or a sip of coffee is sometimes painful or brushing or flossing makes you wince occasionally, you may have a common problem called 'sensitive teeth'. Some of the causes include tooth decay, a cracked tooth, worn tooth enamel, worn fillings and tooth roots that are exposed as a result of aggressive tooth brushing, gum recession and periodontal (gum) disease.</p>
   
                                         <p><b>Symptoms of Sensitive Teeth</b></p>

                                         <p>A layer of enamel, the strongest substance in the body, protects the crowns of healthy teeth. A layer called cementum protects the tooth under the gum line. Underneath the enamel and the cementum is dentin, a part of the tooth that is less dense than enamel or cementum.</p>

                                         <p>The dentin contains microscopic tubules (small hollow tubes or canals). When the dentin loses its protective covering, the tubules allow heat and cold or acidic or sticky foods to stimulate the nerves and cells inside the tooth. This may cause hypersensitivity and occasional discomfort when you chew, drink cold or hot liquids, or when you breathe through your mouth.</p>
                                         
                                         <p>Proper oral hygiene is an important step in preventing tooth decay, periodontal disease and pain from sensitive teeth. If you brush your teeth incorrectly or brush too aggressively, you may injure your gums and expose tooth roots.</p>

                                         <p>Periodontal disease - an infection of the gums and bone that support the teeth - also may be responsible. If periodontal disease is left untreated, gum tissues can separate from the teeth and form spaces called pockets that provide a home for bacteria. Periodontal disease can progress until the bone and other tooth supporting tissues are destroyed, leaving the root surfaces of the teeth exposed. Regular dental check ups are important so that the tooth decay, periodontal disease and other problems may be detected and treated in the early stages.</p>

                                         <p><b>Treatment for Sensitive Teeth in Pune</b></p>

                                         <p>Sensitive teeth can be treated. Depending on the cause, dentists may suggest that you try desensitizing toothpaste which contains compounds that help block sensation traveling from the tooth surface to the nerve. Desensitising toothpaste usually requires several applications before the sensitivity is reduced.</p>

                                         <p>If the desensitising toothpaste does not ease your discomfort, we participating dentists may suggest other treatment. A fluoride gel or special desensitizing agents may be applied to the sensitive areas of the affected tooth. When these measures do not correct the problem, we participating dentists may recommend other treatments such as a filling, a crown, an inlay or bonding to correct a flaw or decay that results in sensitivity.</p>

                                         <p>If the gum tissue has been lost from the root (gum recession), we participating dentists may recommend a surgical gum graft to cover the root, protect the tooth and reduce the sensitivity.</p>


 
										
