                                        <p><b>What Is Gum Disease?</b></p>
  
                                         <p>Gum disease treatment in Pune is when harmful bacteria collect in the pockets around your teeth and cause damage there. The mild form is called gingivitis. It causes your gums to get red and swollen. Some people have chronic gingivitis that never develops into more serious conditions, but most often gingivitis is a sign that you may be in danger of further damage and lost teeth.</p>
                                         
                                         <p>The more severe form of gum disease, called periodontitis, is when you get larger colonies of bacteria in your gum pockets. Their harmful secretions damage your gum tissue, causing it to become red, inflamed, and bloody. You lose gum tissue, as the pockets deepen, making room for more bacteria. Eventually, the pockets get deep enough that the bacteria can attack your jawbone and the ligaments that hold your teeth in place, resulting in tooth loss. In severe cases, the infection can spread to your ears, sinuses, and even brain, which can be fatal. </p> 

