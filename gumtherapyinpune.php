                                                <li>Stroke</li>
                                                <li>Rheumatoid arthritis</li>
                                                <li>Pregnancy complications</li>
                                                <li>Respiratory disease</li>
                                                <li>Diabetes</li>
                                            </ul>

                                            
                                            <p>Diabetes and gum disease seem to have a dynamic relationship in which each one makes the other worse. Developing gum disease seems to make it harder to keep blood sugar levels under control, while uncontrolled blood sugar levels increase the risk for gum disease.</p>

