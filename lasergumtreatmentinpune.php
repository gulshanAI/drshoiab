<p><b>5.	Laser Pocket Disinfection(LPD) :</b></br>
Periodontal disease is caused by the bacteria that live around the teeth and within the gum tissues. By practicing good oral hygiene and receiving regular professional care at the dental office, the bacteria living around the teeth can be kept in check, but the bacteria living in the gum tissues stay behind. So even by keeping the teeth relatively free of bacteria, bacteria living in the gum tissues can still cause inflammation and periodontitis. In the past, the gum tissue bacteria could only be removed by performing gum surgeries, which involves surgical removal of the diseased gum tissue with a scalpel, requiring stitches post operatively.</br>
